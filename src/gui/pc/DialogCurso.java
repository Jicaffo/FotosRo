    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed

        //Guardar curso
        String nombreCurso = fieldNombreCurso.getText();
        String turno = "Mañana";
        if (rbTarde.isSelected()) {
            turno = rbTarde.getText();
        } else if (rbAmbos.isSelected()) {
            turno = rbAmbos.getText();
        }

        Curso c;
        if (chkFotoSacada.isSelected()) {
            String archivoFotoGrupal = fieldNroFoto.getText();
            Date fechaFotoGrupal = Date.valueOf(fieldFechaFoto.getText()); //Tiene que estar en formato aaaa-[m]m-[d]d

            c = new Curso(nombreCurso, turno, archivoFotoGrupal, fechaFotoGrupal);

        } else {

            c = new Curso(nombreCurso, turno);
        }

        bd.setUrlBD("jdbc:sqlite:BD/2018/Prueba1.db");
        bd.insertarCursoSinFoto(c);

        //Guardar Alumnos (recuperar primero el ID del curso recién insertado)
        if (!arrayAlumnos.isEmpty()) {
            int idCurso = bd.obtenerCursoBD(c).getIdCurso();
            for (Alumno a : arrayAlumnos) {
                a.setIdCursoAlumno(idCurso);
            }
            bd.insertarAlumnos(arrayAlumnos);
        }

        this.dispose();


    }//GEN-LAST:event_btGuardarActionPerformed

