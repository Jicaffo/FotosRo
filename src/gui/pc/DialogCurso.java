    private void fieldNuevoAlumnoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fieldNuevoAlumnoKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            Alumno a = new Alumno(fieldNuevoAlumno.getText());
            arrayAlumnos.add(a);
            modeloListaAlumnos.addElement(a);
            fieldNuevoAlumno.setText("");
        }
    }//GEN-LAST:event_fieldNuevoAlumnoKeyReleased

    private void btModificarAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btModificarAlumnoActionPerformed
        indexAlumnoSeleccionado = listaAlumnos.getSelectedIndex();
        if (indexAlumnoSeleccionado == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un alumno de la lista para poder modificarlo.");
        } else {

            String nuevoNombre = JOptionPane.showInputDialog("Ingresa el nuevo nombre para el alumno",
                    arrayAlumnos.get(indexAlumnoSeleccionado).getNombreAlumno());
            
            arrayAlumnos.get(indexAlumnoSeleccionado).setNombreAlumno(nuevoNombre);
            modeloListaAlumnos.get(indexAlumnoSeleccionado).setNombreAlumno(nuevoNombre);
        }
    }//GEN-LAST:event_btModificarAlumnoActionPerformed

    private void btBorrarAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBorrarAlumnoActionPerformed
        indexAlumnoSeleccionado = listaAlumnos.getSelectedIndex();
        if (indexAlumnoSeleccionado == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un alumno de la lista para borrarlo.");
        } else {
            modeloListaAlumnos.remove(indexAlumnoSeleccionado);
            arrayAlumnos.remove(indexAlumnoSeleccionado);
        }
    }//GEN-LAST:event_btBorrarAlumnoActionPerformed

